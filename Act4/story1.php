<p>The Tell-Tale Heart by Edgar Allan Poe is a short horror story about a narrator who insists on their sanity while describing a terrible crime.</p>

<p>The narrator becomes obsessed with an old man’s “vulture eye,” which they find disturbing. Determined to get rid of it, the narrator carefully plans and eventually murders the old man in his sleep. After the crime, they dismember the body and hide it under the floorboards.</p>

<p>When the police arrive to investigate a noise complaint, the narrator calmly invites them in and even places their chair directly above where the body is hidden. However, as the conversation continues, the narrator begins to hear a faint beating sound, which grows louder and louder. Believing it to be the old man’s still-beating heart, the narrator becomes overwhelmed with guilt and paranoia.</p>
<p>Unable to bear the sound any longer, the narrator confesses to the crime, revealing the hidden body.</p>

